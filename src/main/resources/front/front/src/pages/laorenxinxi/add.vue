  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            shenqingbianhao : false,
            yanglaoyuanmingcheng : false,
            feiyong : false,
            laorenxingming : false,
            laorenzhaopian : false,
            shenfenzheng : false,
            laorenxingbie : false,
            laorennianling : false,
            yonghuzhanghao : false,
