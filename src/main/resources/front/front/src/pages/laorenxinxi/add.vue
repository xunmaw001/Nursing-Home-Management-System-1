            lianxifangshi : false,
            shenqingshijian : false,
            shenqingneirong : false,
            sfsh : false,
            shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          shenqingbianhao: this.getUUID(),
          yanglaoyuanmingcheng: '',
          feiyong: '',
          laorenxingming: '',
          laorenzhaopian: '',
          shenfenzheng: '',
          laorenxingbie: '',
          laorennianling: '',
          yonghuzhanghao: '',
          yonghuxingming: '',
          lianxifangshi: '',
          shenqingshijian: '',
          shenqingneirong: '',
        },
        yanglaoyuanmingchengOptions: [],
        laorenxingbieOptions: [],
        rules: {
          shenqingbianhao: [
          ],
          yanglaoyuanmingcheng: [
          ],
          feiyong: [
          ],
          laorenxingming: [
          ],
          laorenzhaopian: [
          ],
          shenfenzheng: [
          ],
          laorenxingbie: [
          ],
          laorennianling: [
          ],
          yonghuzhanghao: [
          ],
          yonghuxingming: [
          ],
          lianxifangshi: [
          ],
          shenqingshijian: [
          ],
          shenqingneirong: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.shenqingshijian = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='shenqingbianhao'){
              this.ruleForm.shenqingbianhao = obj[o];
              this.ro.shenqingbianhao = true;
              continue;
            }
            if(o=='yanglaoyuanmingcheng'){
              this.ruleForm.yanglaoyuanmingcheng = obj[o];
              this.ro.yanglaoyuanmingcheng = true;
              continue;
            }
            if(o=='feiyong'){
              this.ruleForm.feiyong = obj[o];
              this.ro.feiyong = true;
              continue;
            }
            if(o=='laorenxingming'){
              this.ruleForm.laorenxingming = obj[o];
              this.ro.laorenxingming = true;
              continue;
            }
            if(o=='laorenzhaopian'){
              this.ruleForm.laorenzhaopian = obj[o].split(",")[0];
              this.ro.laorenzhaopian = true;
              continue;
            }
            if(o=='shenfenzheng'){
              this.ruleForm.shenfenzheng = obj[o];
              this.ro.shenfenzheng = true;
              continue;
            }
            if(o=='laorenxingbie'){
              this.ruleForm.laorenxingbie = obj[o];
              this.ro.laorenxingbie = true;
              continue;
            }
            if(o=='laorennianling'){
              this.ruleForm.laorennianling = obj[o];
              this.ro.laorennianling = true;
              continue;
            }
            if(o=='yonghuzhanghao'){
              this.ruleForm.yonghuzhanghao = obj[o];
              this.ro.yonghuzhanghao = true;
              continue;
            }
            if(o=='yonghuxingming'){
              this.ruleForm.yonghuxingming = obj[o];
              this.ro.yonghuxingming = true;
              continue;
            }
            if(o=='lianxifangshi'){
              this.ruleForm.lianxifangshi = obj[o];
              this.ro.lianxifangshi = true;
              continue;
            }
            if(o=='shenqingshijian'){
              this.ruleForm.shenqingshijian = obj[o];
              this.ro.shenqingshijian = true;
              continue;
            }
            if(o=='shenqingneirong'){
              this.ruleForm.shenqingneirong = obj[o];
              this.ro.shenqingneirong = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.yonghuzhanghao!=''&&json.yonghuzhanghao) || json.yonghuzhanghao==0){
                this.ruleForm.yonghuzhanghao = json.yonghuzhanghao
            }
            if((json.yonghuxingming!=''&&json.yonghuxingming) || json.yonghuxingming==0){
                this.ruleForm.yonghuxingming = json.yonghuxingming
            }
            if((json.lianxifangshi!=''&&json.lianxifangshi) || json.lianxifangshi==0){
                this.ruleForm.lianxifangshi = json.lianxifangshi
            }
          }
        });
        this.$http.get('option/yanglaoyuanxinxi/yanglaoyuanmingcheng', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.yanglaoyuanmingchengOptions = res.data.data;
          }
        });
        this.laorenxingbieOptions = "男,女".split(',')
      },
      // 下二随
      yanglaoyuanmingchengChange () {
        this.$http.get('follow/yanglaoyuanxinxi/yanglaoyuanmingcheng?columnValue=' + this.ruleForm.yanglaoyuanmingcheng, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.feiyong){
              this.ruleForm.feiyong = res.data.data.feiyong
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('laorenxinxi/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('laorenxinxi/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('laorenxinxi/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('laorenxinxi/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      laorenzhaopianUploadChange(fileUrls) {
          this.ruleForm.laorenzhaopian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

